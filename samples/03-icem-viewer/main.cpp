// 03-icem-viewer — the ICEM viewer skeleton (brick B1). It loads a computed part (a binary STL, e.g.
// ICEM's brick6 thrust chamber) and draws it depth-correct and lit, orbitable with the mouse. Two
// modes, like the other samples:
//   * windowed (default): present the lit, orbitable part via the swapchain — drag to orbit, right-
//     drag to pan, wheel to zoom, F to reframe, ESC to quit;
//   * off-screen (--offscreen [file.ppm]): render one frame to a file (and print coverage) — runnable
//     anywhere, GPU-free in CI. tests/rhi/mesh_offscreen_test asserts on this exact render.
//
// Run it:   build/dev/bin/icem_viewer part.stl
//           build/dev/bin/icem_viewer part.stl --offscreen view.ppm --size 512
//           build/dev/bin/icem_viewer            # no file → a unit cube, so it runs out of the box

