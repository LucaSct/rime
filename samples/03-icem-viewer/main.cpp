namespace {

using rime::viewer::CpuMesh;
using rime::viewer::MeshPush;
using rime::viewer::OrbitCamera;

constexpr rime::rhi::ClearColor kClear{0.08f, 0.09f, 0.11f, 1.0f}; // dark studio background

// Point the camera at the part: frame its bounding sphere, set near/far to bracket it, and start from
// a pleasant three-quarter view. ICEM authors parts z-up (cylinders/revolves about z), so that is the
// default orbit axis.
void setup_camera(OrbitCamera& cam, const CpuMesh& mesh, float aspect, rime::core::Vec3 world_up) {
    const float r = std::max(mesh.radius(), 1e-4f);
    cam.world_up = world_up;
    cam.z_near = std::max(r * 0.02f, 1e-4f);
    cam.z_far = r * 100.0f + 10.0f;
    cam.min_distance = r * 0.05f;
    cam.max_distance = r * 100.0f;
    cam.frame(mesh.center(), r, aspect);
    cam.yaw = rime::core::radians(35.0f);
    cam.pitch = rime::core::radians(20.0f);
}

MeshPush make_push(const OrbitCamera& cam, float aspect) {
    MeshPush p{};
    p.mvp = cam.view_proj(aspect);
    const rime::core::Vec3 e = cam.eye();
    p.cam_pos[0] = e.x;
    p.cam_pos[1] = e.y;
    p.cam_pos[2] = e.z;
    p.cam_pos[3] = 1.0f;
    return p;
}

bool write_ppm(const std::string& path,
               const std::vector<std::uint8_t>& rgba,
               std::uint32_t w,
               std::uint32_t h) {
    std::ofstream out(path, std::ios::binary);
    if (!out) return false;
    out << "P6\n" << w << ' ' << h << "\n255\n";
    for (std::size_t i = 0; i < static_cast<std::size_t>(w) * h; ++i) {
        out.write(reinterpret_cast<const char*>(&rgba[i * 4]), 3); // RGB, drop alpha
    }
    return static_cast<bool>(out);
}

// Off-screen path: render one frame to `out_path` (PPM) and report how much of the frame the part
// covers. No window/display needed — the render the CI gate asserts on.
int run_offscreen(const CpuMesh& mesh,
                  const std::string& out_path,
                  std::uint32_t size,
                  rime::core::Vec3 world_up) {
    rime::rhi::DeviceDesc desc{};
    desc.app_name = "03-icem-viewer";
    auto device = rime::rhi::create_device(desc);
    if (!device) {
        std::fprintf(stderr, "icem_viewer: no Vulkan device available (driver / lavapipe / MoltenVK?)\n");
        return 1;
    }

    OrbitCamera cam;
    setup_camera(cam, mesh, 1.0f, world_up); // square image → aspect 1

    const std::vector<std::uint8_t> px = rime::viewer::render_mesh_offscreen(*device,
                                                                            size,
                                                                            mesh,
                                                                            make_push(cam, 1.0f),
                                                                            kClear,
                                                                            mesh_vert_spv,
                                                                            sizeof(mesh_vert_spv),
                                                                            mesh_frag_spv,
                                                                            sizeof(mesh_frag_spv));

    std::size_t lit = 0;
    for (std::size_t i = 0; i < static_cast<std::size_t>(size) * size; ++i) {
        // A pixel is "part" if it is brighter than the dark background in any channel.
        if (px[i * 4 + 0] > 40 || px[i * 4 + 1] > 40 || px[i * 4 + 2] > 40) ++lit;
    }
    const double pct = 100.0 * static_cast<double>(lit) / (static_cast<double>(size) * size);
    std::printf("icem_viewer: rendered %u triangles on '%s' — part covers %.1f%% of a %ux%u frame\n",
                mesh.triangle_count(),
                device->adapter().name.c_str(),
                pct,
                size,
                size);

    if (!write_ppm(out_path, px, size, size)) {
        std::fprintf(stderr, "icem_viewer: could not write '%s'\n", out_path.c_str());
        return 1;
    }
    std::printf("icem_viewer: wrote %s\n", out_path.c_str());
    return 0;
}

// Create a depth target sized to the swapchain; recreated alongside the swapchain on resize.
rime::rhi::TextureHandle make_depth(rime::rhi::Device& device, rime::rhi::Extent2D extent) {
    rime::rhi::TextureDesc td{};
    td.extent = extent;
    td.format = rime::rhi::Format::D32Float;
    td.usage = rime::rhi::TextureUsage::DepthStencil;
    td.debug_name = "icem-viewer-depth";
    return device.create_texture(td);
}

// Windowed path: present the lit, orbitable part until the window closes (or max_frames is reached).
bool run_windowed(const CpuMesh& mesh, rime::core::Vec3 world_up, int max_frames) {
    using namespace rime::platform;

    if (!init()) return false;
    WindowDesc wd{};
    wd.title = "Rime — ICEM viewer";
    wd.width = 1280;
    wd.height = 720;
    auto window = create_window(wd);
    if (!window) {
        shutdown();
        return false;
    }
    window->show();

    rime::rhi::DeviceDesc dd{};
    dd.app_name = "03-icem-viewer";
    auto device = rime::rhi::create_device(dd);
    if (!device) {
        shutdown();
        return false;
    }

    rime::rhi::SwapchainDesc sd{};
    sd.window = window->native_handle();
    const Extent2D fb = window->framebuffer_size();
    sd.extent = {fb.width, fb.height};
    auto swapchain = device->create_swapchain(sd);
    if (!swapchain) {
        device.reset();
        shutdown();
        return false;
    }

    auto gpu = rime::viewer::make_mesh(*device,
                                       swapchain->format(),
                                       rime::rhi::Format::D32Float,
                                       mesh,
                                       mesh_vert_spv,
                                       sizeof(mesh_vert_spv),
                                       mesh_frag_spv,
                                       sizeof(mesh_frag_spv));
    rime::rhi::TextureHandle depth = make_depth(*device, swapchain->extent());

    OrbitCamera cam;
    {
        const rime::rhi::Extent2D e = swapchain->extent();
        setup_camera(cam, mesh, static_cast<float>(e.width) / static_cast<float>(e.height), world_up);
    }

    std::printf("icem_viewer: presenting %u triangles on '%s'. Drag=orbit, right-drag=pan, "
                "wheel=zoom, F=reframe, ESC=quit.\n",
                mesh.triangle_count(),
                device->adapter().name.c_str());

    Input input;
    int frames = 0;
    while (pump_events() && !window->should_close()) {
        if (max_frames > 0 && frames >= max_frames) break;

        input.new_frame();
        Event e{};
        while (poll_event(e)) input.process(e);
        if (input.key_pressed(Key::Escape)) window->request_close();

        const rime::rhi::Extent2D ext = swapchain->extent();
        const float h = static_cast<float>(ext.height);
        const float aspect = static_cast<float>(ext.width) / h;
        if (input.mouse_down(MouseButton::Left)) {
            cam.orbit(input.mouse_dx() * 0.008f, -input.mouse_dy() * 0.008f);
        }
        if (input.mouse_down(MouseButton::Right) || input.mouse_down(MouseButton::Middle)) {
            cam.pan(input.mouse_dx() / h, input.mouse_dy() / h);
        }
        if (input.wheel_y() != 0.0f) cam.zoom(input.wheel_y());
        if (input.key_pressed(Key::F)) cam.frame(mesh.center(), std::max(mesh.radius(), 1e-4f), aspect);

        rime::rhi::TextureHandle target = swapchain->acquire_next_image();
        if (!target.is_valid()) {
            device->wait_idle();
            const Extent2D s = window->framebuffer_size();
            swapchain->recreate({s.width, s.height});
            device->destroy(depth);
            depth = make_depth(*device, swapchain->extent());
            continue;
        }

        auto cmd = device->begin_commands();
        rime::viewer::record_mesh(*cmd, gpu, target, depth, ext, make_push(cam, aspect), kClear);
        if (!swapchain->present(*cmd)) {
            device->wait_idle();
            const Extent2D s = window->framebuffer_size();
            swapchain->recreate({s.width, s.height});
            device->destroy(depth);
            depth = make_depth(*device, swapchain->extent());
        }
        ++frames;
    }

    device->wait_idle();
    device->destroy(depth);
    rime::viewer::destroy_mesh(*device, gpu);
    swapchain.reset();
    device.reset();
    shutdown();
    std::printf("icem_viewer: presented %d frame%s.\n", frames, frames == 1 ? "" : "s");
    return true;
}

} // namespace

int main(int argc, char** argv) {
    std::string stl_path;
    std::string out_path = "icem_view.ppm";
    bool offscreen = false;
    std::uint32_t size = 512;
    int frames = 0;
    rime::core::Vec3 world_up{0.0f, 0.0f, 1.0f}; // ICEM parts are authored z-up

    for (int i = 1; i < argc; ++i) {
        const std::string_view a(argv[i]);
        if (a == "--offscreen" || a == "--headless") {
            offscreen = true;
            if (i + 1 < argc && argv[i + 1][0] != '-') out_path = argv[++i];
        } else if (a == "--windowed") {
            offscreen = false;
        } else if (a == "--size" && i + 1 < argc) {
            size = static_cast<std::uint32_t>(std::max(8, std::atoi(argv[++i])));
        } else if (a == "--frames" && i + 1 < argc) {
            frames = std::atoi(argv[++i]);
        } else if (a == "--up-y") {
            world_up = {0.0f, 1.0f, 0.0f};
        } else if (!a.empty() && a[0] != '-') {
            stl_path = a;
        }
    }

    CpuMesh mesh;
    if (!stl_path.empty()) {
        auto loaded = rime::viewer::load_stl_binary_file(stl_path);
        if (!loaded) {
            std::fprintf(stderr, "icem_viewer: could not load binary STL '%s'\n", stl_path.c_str());
            return 1;
        }
        mesh = std::move(*loaded);
    } else {
        std::printf("icem_viewer: no STL given — showing a unit cube. Pass a binary STL to view a part.\n");
        mesh = rime::viewer::make_unit_cube();
    }

    if (offscreen) return run_offscreen(mesh, out_path, size, world_up);
    if (run_windowed(mesh, world_up, frames)) return 0;
    std::printf("icem_viewer: no window/display — falling back to off-screen (%s).\n", out_path.c_str());
    return run_offscreen(mesh, out_path, size, world_up);
}
