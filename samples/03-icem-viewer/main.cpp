constexpr rime::rhi::ClearColor kClear{0.08f, 0.09f, 0.11f, 1.0f}; // dark studio background

// Point the camera at the part: frame its bounding sphere, set near/far to bracket it, and start
// from a pleasant three-quarter view. ICEM authors parts z-up (cylinders/revolves about z), so that
// is the default orbit axis.
void setup_camera(OrbitCamera& cam, const CpuMesh& mesh, float aspect, rime::core::Vec3 world_up) {
    const float r = std::max(mesh.radius(), 1e-4f);
    cam.world_up = world_up;
    cam.z_near = std::max(r * 0.02f, 1e-4f);
    cam.z_far = r * 100.0f + 10.0f;
    cam.min_distance = r * 0.05f;
    cam.max_distance = r * 100.0f;
    cam.frame(mesh.center(), r, aspect);
    cam.yaw = rime::core::radians(35.0f);
    cam.pitch = rime::core::radians(20.0f);
}

// Interactive cross-section state: a world-axis-aligned plane that cuts the part open. `axis` picks
// the plane normal (x/y/z), `sign` which half is removed, `offset` where along the axis the plane
// sits.
struct Clip {
    bool on = false;
    int axis = 0;        // 0 = x, 1 = y, 2 = z
    float sign = 1.0f;   // +1 removes the +axis half, -1 the −axis half
    float offset = 0.0f; // plane position along the axis, in world units
};

rime::core::Vec3 axis_unit(int axis) {
    return axis == 0   ? rime::core::Vec3{1, 0, 0}
           : axis == 1 ? rime::core::Vec3{0, 1, 0}
                       : rime::core::Vec3{0, 0, 1};
}

// Fill a (scale, bias) push pair from a loaded field, or zero it (which the shader reads as "no
// field": the neutral metal albedo). vmin/vmax ride in the .w components; the field is "on" iff
// vmax > vmin.
void fill_field(float scale[4], float bias[4], const ScalarField* field, bool on) {
    if (field && on) {
        for (int c = 0; c < 3; ++c) {
            scale[c] = field->scale[c];
            bias[c] = field->bias[c];
        }
        scale[3] = field->vmin;
        bias[3] = field->vmax;
    } else {
        scale[0] = scale[1] = scale[2] = scale[3] = 0.0f;
        bias[0] = bias[1] = bias[2] = bias[3] = 0.0f;
    }
}

void set_field_push(MeshPush& p, const ScalarField* field, bool on) {
    fill_field(p.field_scale, p.field_bias, field, on);
}

MeshPush make_push(const OrbitCamera& cam,
                   float aspect,
                   const Clip& clip,
                   const ScalarField* field,
                   bool field_on) {
    MeshPush p{};
    p.mvp = cam.view_proj(aspect);
    const rime::core::Vec3 e = cam.eye();
    p.cam_pos[0] = e.x;
    p.cam_pos[1] = e.y;
    p.cam_pos[2] = e.z;
    p.cam_pos[3] = 1.0f;
    if (clip.on) {
        // normal = sign·axis, offset w = sign·offset → discard where dot(N,p) > w cuts the chosen
        // half at coordinate `offset` (see the derivation in mesh_render.hpp / the shader).
        const rime::core::Vec3 n = axis_unit(clip.axis) * clip.sign;
        p.clip_plane[0] = n.x;
        p.clip_plane[1] = n.y;
        p.clip_plane[2] = n.z;
        p.clip_plane[3] = clip.sign * clip.offset;
    } else {
        p.clip_plane[0] = p.clip_plane[1] = p.clip_plane[2] = 0.0f;
        p.clip_plane[3] = 1e30f; // disabled: nothing is clipped
    }
    set_field_push(p, field, field_on);
    return p;
}

// Build the cap-quad push: the camera matrix, the same field colormap params as the surface, and
// the cut rectangle — a square of side ~2·radius on the cutting plane, centred on the part (the
// stencil trims it to the solid). axis/offset come from the clip.
rime::viewer::CapPush make_cap_push(const OrbitCamera& cam,
                                    float aspect,
                                    const Clip& clip,
                                    const ScalarField* field,
                                    bool field_on,
                                    const CpuMesh& mesh) {
    rime::viewer::CapPush c{};
    c.mvp = cam.view_proj(aspect);
    fill_field(c.field_scale, c.field_bias, field, field_on);

    const rime::core::Vec3 ctr = mesh.center();
    const float r = std::max(mesh.radius(), 1e-4f) * 1.05f;
    const auto comp = [](const rime::core::Vec3& p, int i) {
        return i == 0 ? p.x : i == 1 ? p.y : p.z;
    };
    const int a = clip.axis;
    const int u = (a == 0) ? 1 : 0; // the two in-plane axes (those that aren't the plane normal)
    const int v = (a == 2) ? 1 : 2;
    c.cap_rect[0] = comp(ctr, u) - r;
    c.cap_rect[1] = comp(ctr, u) + r;
    c.cap_rect[2] = comp(ctr, v) - r;
    c.cap_rect[3] = comp(ctr, v) + r;
    c.cap_meta[0] = clip.offset;
    c.cap_meta[1] = static_cast<float>(a);
    return c;
}

bool write_ppm(const std::string& path,
               const std::vector<std::uint8_t>& rgba,
               std::uint32_t w,
               std::uint32_t h) {
    std::ofstream out(path, std::ios::binary);
    if (!out)
        return false;
    out << "P6\n" << w << ' ' << h << "\n255\n";
    for (std::size_t i = 0; i < static_cast<std::size_t>(w) * h; ++i) {
        out.write(reinterpret_cast<const char*>(&rgba[i * 4]), 3); // RGB, drop alpha
    }
    return static_cast<bool>(out);
}

// Render one lit, square (aspect-1) frame of the part at camera `cam` to an RGBA8 pixel buffer. The one
// place that dispatches plain vs. sectioned (capped) rendering, shared by the single snapshot, the
// turntable export and the windowed screenshot — so all three stay pixel-for-pixel the same view. A
// sectioned frame carries the solid cap (and the field on the cut face); an unclipped one is the plain
// lit part. `cap_on` is ignored unless `clip.on`.
std::vector<std::uint8_t> render_view(rime::rhi::Device& device,
                                      const CpuMesh& mesh,
                                      const OrbitCamera& cam,
                                      std::uint32_t size,
                                      const Clip& clip,
                                      const ScalarField* field,
                                      bool field_on,
                                      bool cap_on) {
    const bool use_field = field != nullptr && field_on;
    const float* frgba = use_field ? field->rgba.data() : nullptr;
    const std::uint32_t fnx = use_field ? field->nx : 1;
    const std::uint32_t fny = use_field ? field->ny : 1;
    const std::uint32_t fnz = use_field ? field->nz : 1;
    if (clip.on) {
        return rime::viewer::render_section_offscreen(
            device, size, mesh, make_push(cam, 1.0f, clip, field, field_on),
            make_cap_push(cam, 1.0f, clip, field, field_on, mesh), kClear, cap_on, mesh_vert_spv,
            sizeof(mesh_vert_spv), mesh_frag_spv, sizeof(mesh_frag_spv), capmark_frag_spv,
            sizeof(capmark_frag_spv), cap_vert_spv, sizeof(cap_vert_spv), cap_frag_spv,
            sizeof(cap_frag_spv), frgba, fnx, fny, fnz);
    }
    return rime::viewer::render_mesh_offscreen(
        device, size, mesh, make_push(cam, 1.0f, clip, field, field_on), kClear, mesh_vert_spv,
        sizeof(mesh_vert_spv), mesh_frag_spv, sizeof(mesh_frag_spv), frgba, fnx, fny, fnz);
}

// Off-screen path: render one frame to `out_path` (PPM) and report how much of the frame the part
// covers. No window/display needed — the render the CI gate asserts on.
int run_offscreen(const CpuMesh& mesh,
                  const std::string& out_path,
                  std::uint32_t size,
                  rime::core::Vec3 world_up,
                  Clip clip,
                  const ScalarField* field,
                  bool field_on,
                  bool cap_on) {
    rime::rhi::DeviceDesc desc{};
    desc.app_name = "03-icem-viewer";
    auto device = rime::rhi::create_device(desc);
    if (!device) {
        std::fprintf(stderr,
                     "icem_viewer: no Vulkan device available (driver / lavapipe / MoltenVK?)\n");
        return 1;
    }

    OrbitCamera cam;
    setup_camera(cam, mesh, 1.0f, world_up); // square image → aspect 1

    // A sectioned snapshot cuts through the part center along the chosen axis.
    if (clip.on)
        clip.offset = rime::core::dot(mesh.center(), axis_unit(clip.axis));

    const bool use_field = field != nullptr && field_on;
    const std::vector<std::uint8_t> px =
        render_view(*device, mesh, cam, size, clip, field, field_on, cap_on);

    std::size_t lit = 0;
    for (std::size_t i = 0; i < static_cast<std::size_t>(size) * size; ++i) {
        // A pixel is "part" if it is brighter than the dark background in any channel.
        if (px[i * 4 + 0] > 40 || px[i * 4 + 1] > 40 || px[i * 4 + 2] > 40)
            ++lit;
    }
    const double pct = 100.0 * static_cast<double>(lit) / (static_cast<double>(size) * size);
    std::printf(
        "icem_viewer: rendered %u triangles on '%s'%s — part covers %.1f%% of a %ux%u frame\n",
        mesh.triangle_count(),
        device->adapter().name.c_str(),
        use_field ? " (field colormap)" : "",
        pct,
        size,
        size);

    if (!write_ppm(out_path, px, size, size)) {
        std::fprintf(stderr, "icem_viewer: could not write '%s'\n", out_path.c_str());
        return 1;
    }
    std::printf("icem_viewer: wrote %s\n", out_path.c_str());
    return 0;
}

// Turntable export (E4): render `frames` lit snapshots orbiting the part through a full 360°, one PPM
// each (numbered off the out template — icem_view.ppm → icem_view_000.ppm …), and report the render
// throughput. Headless by nature — a showcase loop over the proven off-screen path, reusing one device.
// The "perf" half: each frame is timed, and the min / avg / max ms and the frames-per-second print at the
// end, an honest measure of the engine's offscreen render+readback rate on this GPU.
int run_turntable_offscreen(const CpuMesh& mesh,
                            const std::string& out_path,
                            std::uint32_t size,
                            rime::core::Vec3 world_up,
                            Clip clip,
                            const ScalarField* field,
                            bool field_on,
                            bool cap_on,
                            int frames) {
    frames = std::max(frames, 1);
    rime::rhi::DeviceDesc desc{};
    desc.app_name = "03-icem-viewer";
    auto device = rime::rhi::create_device(desc);
    if (!device) {
        std::fprintf(stderr, "icem_viewer: no Vulkan device available\n");
        return 1;
    }

    OrbitCamera cam;
    setup_camera(cam, mesh, 1.0f, world_up);
    if (clip.on)
        clip.offset = rime::core::dot(mesh.center(), axis_unit(clip.axis));
    const float yaw0 = cam.yaw;

    std::printf("icem_viewer: turntable — %d frames of %u triangles at %ux%u on '%s'%s%s …\n",
                frames,
                mesh.triangle_count(),
                size,
                size,
                device->adapter().name.c_str(),
                (field && field_on) ? " (field colormap)" : "",
                clip.on ? " (sectioned)" : "");

    double t_sum = 0.0, t_min = 1e30, t_max = 0.0;
    for (int i = 0; i < frames; ++i) {
        cam.yaw = rime::viewer::turntable_yaw(i, frames, yaw0);
        const auto t0 = std::chrono::steady_clock::now();
        const std::vector<std::uint8_t> px =
            render_view(*device, mesh, cam, size, clip, field, field_on, cap_on);
        const double ms =
            std::chrono::duration<double, std::milli>(std::chrono::steady_clock::now() - t0).count();
        t_sum += ms;
        t_min = std::min(t_min, ms);
        t_max = std::max(t_max, ms);

        const std::string frame_path = rime::viewer::turntable_frame_path(out_path, i);
        if (!write_ppm(frame_path, px, size, size)) {
            std::fprintf(stderr, "icem_viewer: could not write '%s'\n", frame_path.c_str());
            return 1;
        }
    }

    const double avg = t_sum / frames;
    std::printf("icem_viewer: wrote %s … %s — %.1f ms/frame avg (%.1f .. %.1f), %.1f fps\n",
                rime::viewer::turntable_frame_path(out_path, 0).c_str(),
                rime::viewer::turntable_frame_path(out_path, frames - 1).c_str(),
                avg,
                t_min,
                t_max,
                avg > 0.0 ? 1000.0 / avg : 0.0);
    return 0;
}

// Create a depth-stencil target sized to the swapchain; recreated alongside the swapchain on
// resize. D32FloatS8 (depth + stencil) so the cross-section cap's stencil pass has somewhere to
// write (ADR-0014).
rime::rhi::TextureHandle make_depth(rime::rhi::Device& device, rime::rhi::Extent2D extent) {
    rime::rhi::TextureDesc td{};
    td.extent = extent;
    td.format = rime::rhi::Format::D32FloatS8;
    td.usage = rime::rhi::TextureUsage::DepthStencil;
    td.debug_name = "icem-viewer-depthstencil";
    return device.create_texture(td);
}

// Windowed path: present the lit, orbitable part until the window closes (or max_frames is
// reached).
bool run_windowed(const CpuMesh& mesh,
                  rime::core::Vec3 world_up,
                  int max_frames,
                  const ScalarField* field,
                  bool field_on,
                  bool cap_on) {
    using namespace rime::platform;

    if (!init())
        return false;
    WindowDesc wd{};
    wd.title = "Rime — ICEM viewer";
    wd.width = 1280;
    wd.height = 720;
    auto window = create_window(wd);
    if (!window) {
        shutdown();
        return false;
    }
    window->show();

    rime::rhi::DeviceDesc dd{};
    dd.app_name = "03-icem-viewer";
    auto device = rime::rhi::create_device(dd);
    if (!device) {
        shutdown();
        return false;
    }

    rime::rhi::SwapchainDesc sd{};
    sd.window = window->native_handle();
    const Extent2D fb = window->framebuffer_size();
    sd.extent = {fb.width, fb.height};
    auto swapchain = device->create_swapchain(sd);
    if (!swapchain) {
        device.reset();
        shutdown();
        return false;
    }

    // Upload the field volume once (if any); toggling G only flips the cheap push-constant flag.
    const float* frgba = field ? field->rgba.data() : nullptr;
    const std::uint32_t fnx = field ? field->nx : 1;
    const std::uint32_t fny = field ? field->ny : 1;
    const std::uint32_t fnz = field ? field->nz : 1;
    auto gpu = rime::viewer::make_mesh(*device,
                                       swapchain->format(),
                                       rime::rhi::Format::D32FloatS8,
                                       mesh,
                                       mesh_vert_spv,
                                       sizeof(mesh_vert_spv),
                                       mesh_frag_spv,
                                       sizeof(mesh_frag_spv),
                                       frgba,
                                       fnx,
                                       fny,
                                       fnz);
    rime::viewer::Cap cap = rime::viewer::make_cap(*device,
                                                   swapchain->format(),
                                                   rime::rhi::Format::D32FloatS8,
                                                   mesh_vert_spv,
                                                   sizeof(mesh_vert_spv),
                                                   capmark_frag_spv,
                                                   sizeof(capmark_frag_spv),
                                                   cap_vert_spv,
                                                   sizeof(cap_vert_spv),
                                                   cap_frag_spv,
                                                   sizeof(cap_frag_spv));
    rime::viewer::Legend legend = rime::viewer::make_legend(*device,
                                                            swapchain->format(),
                                                            legend_vert_spv,
                                                            sizeof(legend_vert_spv),
                                                            legend_frag_spv,
                                                            sizeof(legend_frag_spv));
    rime::rhi::TextureHandle depth = make_depth(*device, swapchain->extent());

    OrbitCamera cam;
    {
        const rime::rhi::Extent2D e = swapchain->extent();
        setup_camera(
            cam, mesh, static_cast<float>(e.width) / static_cast<float>(e.height), world_up);
    }

    Clip clip;
    clip.offset = rime::core::dot(mesh.center(), axis_unit(clip.axis));
    const float clip_step = std::max(std::max(mesh.radius(), 1e-4f) * 0.02f, 1e-5f);
    bool show_field = field != nullptr && field_on;
    bool show_cap =
        cap_on; // a section fills its cut face by default; K toggles the solid cap (B2b)
    int shots = 0; // P-key screenshots taken this session (numbers the output files)

    std::printf(
        "icem_viewer: presenting %u triangles on '%s'.\n"
        "  camera: drag=orbit, right-drag=pan, wheel=zoom, F=reframe, ESC=quit\n"
        "  section: C=toggle, X/Y/Z=axis, N=flip side, [ ]=move plane, K=toggle solid cap\n"
        "  P=screenshot (icem_shot_NNN.ppm)\n",
        mesh.triangle_count(),
        device->adapter().name.c_str());
    if (field) {
        std::printf("  field: '%s' [%s] range %.4g .. %.4g — G=toggle colormap (%s)\n",
                    field->name.c_str(),
                    field->unit.c_str(),
                    static_cast<double>(field->vmin),
                    static_cast<double>(field->vmax),
                    show_field ? "on" : "off");
    }

    Input input;
    int frames = 0;
    while (pump_events() && !window->should_close()) {
        if (max_frames > 0 && frames >= max_frames)
            break;

        input.new_frame();
        Event e{};
        while (poll_event(e))
            input.process(e);
        if (input.key_pressed(Key::Escape))
            window->request_close();

        const rime::rhi::Extent2D ext = swapchain->extent();
        const float h = static_cast<float>(ext.height);
        const float aspect = static_cast<float>(ext.width) / h;
        if (input.mouse_down(MouseButton::Left)) {
            cam.orbit(input.mouse_dx() * 0.008f, -input.mouse_dy() * 0.008f);
        }
        if (input.mouse_down(MouseButton::Right) || input.mouse_down(MouseButton::Middle)) {
            cam.pan(input.mouse_dx() / h, input.mouse_dy() / h);
        }
        if (input.wheel_y() != 0.0f)
            cam.zoom(input.wheel_y());
        if (input.key_pressed(Key::F))
            cam.frame(mesh.center(), std::max(mesh.radius(), 1e-4f), aspect);

        // Cross-section controls. Selecting an axis re-centers the plane on the part.
        if (input.key_pressed(Key::C))
            clip.on = !clip.on;
        if (input.key_pressed(Key::X)) {
            clip.axis = 0;
            clip.offset = rime::core::dot(mesh.center(), axis_unit(0));
        }
        if (input.key_pressed(Key::Y)) {
            clip.axis = 1;
            clip.offset = rime::core::dot(mesh.center(), axis_unit(1));
        }
        if (input.key_pressed(Key::Z)) {
            clip.axis = 2;
            clip.offset = rime::core::dot(mesh.center(), axis_unit(2));
        }
        if (input.key_pressed(Key::N))
            clip.sign = -clip.sign;
        if (input.key_down(Key::LeftBracket))
            clip.offset -= clip_step;
        if (input.key_down(Key::RightBracket))
            clip.offset += clip_step;
        // Field colormap toggle (only meaningful when a field was loaded).
        if (input.key_pressed(Key::G) && field)
            show_field = !show_field;
        // Solid-cap toggle for the cross-section (B2b).
        if (input.key_pressed(Key::K))
            show_cap = !show_cap;
        // P: screenshot the live view. Re-render it square at a crisp resolution off-screen (the same
        // render_view the snapshot/turntable use) and write a numbered PPM — no swapchain readback path
        // needed, and the saved frame is higher-res than the window.
        if (input.key_pressed(Key::P)) {
            constexpr std::uint32_t shot_size = 1024;
            const std::vector<std::uint8_t> shot =
                render_view(*device, mesh, cam, shot_size, clip, field, show_field, show_cap);
            const std::string name = rime::viewer::turntable_frame_path("icem_shot.ppm", shots);
            if (write_ppm(name, shot, shot_size, shot_size)) {
                std::printf("icem_viewer: screenshot → %s (%ux%u)\n", name.c_str(), shot_size,
                            shot_size);
                ++shots;
            } else {
                std::fprintf(stderr, "icem_viewer: could not write screenshot '%s'\n", name.c_str());
            }
        }

        rime::rhi::TextureHandle target = swapchain->acquire_next_image();
        if (!target.is_valid()) {
            device->wait_idle();
            const Extent2D s = window->framebuffer_size();
            swapchain->recreate({s.width, s.height});
            device->destroy(depth);
            depth = make_depth(*device, swapchain->extent());
            continue;
        }

        auto cmd = device->begin_commands();
        rime::viewer::record_section(*cmd,
                                     gpu,
                                     cap,
                                     target,
                                     depth,
                                     ext,
                                     make_push(cam, aspect, clip, field, show_field),
                                     make_cap_push(cam, aspect, clip, field, show_field, mesh),
                                     kClear,
                                     clip.on && show_cap);
        if (field && show_field)
            rime::viewer::record_legend(*cmd, legend, target, ext);
        if (!swapchain->present(*cmd)) {
            device->wait_idle();
            const Extent2D s = window->framebuffer_size();
            swapchain->recreate({s.width, s.height});
            device->destroy(depth);
            depth = make_depth(*device, swapchain->extent());
        }
        ++frames;
    }

    device->wait_idle();
    device->destroy(depth);
    rime::viewer::destroy_legend(*device, legend);
    rime::viewer::destroy_cap(*device, cap);
    rime::viewer::destroy_mesh(*device, gpu);
    swapchain.reset();
    device.reset();
    shutdown();
    std::printf("icem_viewer: presented %d frame%s.\n", frames, frames == 1 ? "" : "s");
    return true;
}

// Replace a path's extension (or append, if it has none): so `chamber.stl` pairs with its sibling
// `chamber.icef` / `chamber.icejson`. Returns empty for an empty path.
std::string replace_extension(const std::string& path, const char* ext) {
    if (path.empty())
        return {};
    const std::size_t slash = path.find_last_of("/\\");
    const std::size_t dot = path.find_last_of('.');
    if (dot != std::string::npos && (slash == std::string::npos || dot > slash))
        return path.substr(0, dot) + ext;
    return path + ext;
}

// The sibling computed-field file for an STL path (ICEM's `field.stl` pairs with its `field.icef`).
std::string sibling_icef(const std::string& stl_path) { return replace_extension(stl_path, ".icef"); }

// Off-screen warped snapshot: a static peak-deformation view (gain = 25% of the part radius).
int run_warp_offscreen(const CpuMesh& mesh,
                       const std::string& out_path,
                       std::uint32_t size,
                       rime::core::Vec3 world_up,
                       const VectorField& vf) {
    rime::rhi::DeviceDesc desc{};
    desc.app_name = "03-icem-viewer";
    auto device = rime::rhi::create_device(desc);
    if (!device) {
        std::fprintf(stderr, "icem_viewer: no Vulkan device available\n");
        return 1;
    }
    OrbitCamera cam;
    setup_camera(cam, mesh, 1.0f, world_up);
    const float r = std::max(mesh.radius(), 1e-4f);

    MeshPush p{};
    p.mvp = cam.view_proj(1.0f);
    const rime::core::Vec3 e = cam.eye();
    p.cam_pos[0] = e.x;
    p.cam_pos[1] = e.y;
    p.cam_pos[2] = e.z;
    p.cam_pos[3] = 1.0f;
    p.clip_plane[0] = p.clip_plane[1] = p.clip_plane[2] = 0.0f;
    p.clip_plane[3] = 1e30f;
    for (int c = 0; c < 3; ++c) {
        p.field_scale[c] = vf.scale[c];
        p.field_bias[c] = vf.bias[c];
    }
    p.field_scale[3] = 0.25f * r / vf.vmag_max; // peak warp = 25% radius
    p.field_bias[3] = vf.vmag_max;

    const std::vector<std::uint8_t> px = rime::viewer::render_warp_offscreen(*device,
                                                                             size,
                                                                             mesh,
                                                                             p,
                                                                             kClear,
                                                                             mesh_vert_spv,
                                                                             sizeof(mesh_vert_spv),
                                                                             mesh_frag_spv,
                                                                             sizeof(mesh_frag_spv),
                                                                             warp_vert_spv,
                                                                             sizeof(warp_vert_spv),
                                                                             warp_frag_spv,
                                                                             sizeof(warp_frag_spv),
                                                                             vf.rgba.data(),
                                                                             vf.nx,
                                                                             vf.ny,
                                                                             vf.nz);

    std::size_t lit = 0;
    for (std::size_t i = 0; i < static_cast<std::size_t>(size) * size; ++i)
        if (px[i * 4 + 0] > 40 || px[i * 4 + 1] > 40 || px[i * 4 + 2] > 40)
            ++lit;
    std::printf("icem_viewer: warped %u triangles by field '%s' — covers %.1f%% of a %ux%u frame\n",
                mesh.triangle_count(),
                vf.name.c_str(),
                100.0 * static_cast<double>(lit) / (static_cast<double>(size) * size),
                size,
                size);
    if (!write_ppm(out_path, px, size, size)) {
        std::fprintf(stderr, "icem_viewer: could not write '%s'\n", out_path.c_str());
        return 1;
    }
    std::printf("icem_viewer: wrote %s\n", out_path.c_str());
    return 0;
}

// Windowed warp: animate the surface by the vector field (a modal mode "breathes"), coloured by the
// field magnitude with a legend. M freezes/runs the animation.
bool run_warp_windowed(const CpuMesh& mesh,
                       rime::core::Vec3 world_up,
                       int max_frames,
                       const VectorField& vf) {
    using namespace rime::platform;
    if (!init())
        return false;
    WindowDesc wd{};
    wd.title = "Rime — ICEM viewer (warp)";
    wd.width = 1280;
    wd.height = 720;
    auto window = create_window(wd);
    if (!window) {
        shutdown();
        return false;
    }
    window->show();

    rime::rhi::DeviceDesc dd{};
    dd.app_name = "03-icem-viewer";
    auto device = rime::rhi::create_device(dd);
    if (!device) {
        shutdown();
        return false;
    }
    rime::rhi::SwapchainDesc sd{};
    sd.window = window->native_handle();
    const Extent2D fb = window->framebuffer_size();
    sd.extent = {fb.width, fb.height};
    auto swapchain = device->create_swapchain(sd);
    if (!swapchain) {
        device.reset();
        shutdown();
        return false;
    }

    const rime::rhi::Format depth_fmt = rime::rhi::Format::D32FloatS8;
    auto gpu = rime::viewer::make_mesh(*device,
                                       swapchain->format(),
                                       depth_fmt,
                                       mesh,
                                       mesh_vert_spv,
                                       sizeof(mesh_vert_spv),
                                       mesh_frag_spv,
                                       sizeof(mesh_frag_spv),
                                       vf.rgba.data(),
                                       vf.nx,
                                       vf.ny,
                                       vf.nz);
    rime::viewer::Warp warp = rime::viewer::make_warp(*device,
                                                      swapchain->format(),
                                                      depth_fmt,
                                                      warp_vert_spv,
                                                      sizeof(warp_vert_spv),
                                                      warp_frag_spv,
                                                      sizeof(warp_frag_spv));
    rime::viewer::Legend legend = rime::viewer::make_legend(*device,
                                                            swapchain->format(),
                                                            legend_vert_spv,
                                                            sizeof(legend_vert_spv),
                                                            legend_frag_spv,
                                                            sizeof(legend_frag_spv));
    rime::rhi::TextureHandle depth = make_depth(*device, swapchain->extent());

    OrbitCamera cam;
    {
        const rime::rhi::Extent2D e = swapchain->extent();
        setup_camera(
            cam, mesh, static_cast<float>(e.width) / static_cast<float>(e.height), world_up);
    }
    const float r = std::max(mesh.radius(), 1e-4f);
    const float amp = 0.25f * r; // peak displacement = 25% of the part radius
    const float freq = 0.6f;     // Hz

    std::printf(
        "icem_viewer: warping %u triangles by field '%s' (|max| %.4g) on '%s'.\n"
        "  camera: drag=orbit, right-drag=pan, wheel=zoom, F=reframe, ESC=quit; M=freeze/run\n",
        mesh.triangle_count(),
        vf.name.c_str(),
        static_cast<double>(vf.vmag_max),
        device->adapter().name.c_str());

    Input input;
    int frames = 0;
    bool animate = true;
    float phase = 0.0f; // seconds of animation accumulated (held when frozen)
    auto last = std::chrono::steady_clock::now();
    while (pump_events() && !window->should_close()) {
        if (max_frames > 0 && frames >= max_frames)
            break;
        input.new_frame();
        Event e{};
        while (poll_event(e))
            input.process(e);
        if (input.key_pressed(Key::Escape))
            window->request_close();
        if (input.key_pressed(Key::M))
            animate = !animate;

        const auto now = std::chrono::steady_clock::now();
        const float dt = std::chrono::duration<float>(now - last).count();
        last = now;
        if (animate)
            phase += dt;

        const rime::rhi::Extent2D ext = swapchain->extent();
        const float h = static_cast<float>(ext.height);
        const float aspect = static_cast<float>(ext.width) / h;
        if (input.mouse_down(MouseButton::Left))
            cam.orbit(input.mouse_dx() * 0.008f, -input.mouse_dy() * 0.008f);
        if (input.mouse_down(MouseButton::Right) || input.mouse_down(MouseButton::Middle))
            cam.pan(input.mouse_dx() / h, input.mouse_dy() / h);
        if (input.wheel_y() != 0.0f)
            cam.zoom(input.wheel_y());
        if (input.key_pressed(Key::F))
            cam.frame(mesh.center(), r, aspect);

        const float gain = (amp / vf.vmag_max) * std::sin(2.0f * 3.14159265f * freq * phase);
        MeshPush p{};
        p.mvp = cam.view_proj(aspect);
        const rime::core::Vec3 eye = cam.eye();
        p.cam_pos[0] = eye.x;
        p.cam_pos[1] = eye.y;
        p.cam_pos[2] = eye.z;
        p.cam_pos[3] = 1.0f;
        p.clip_plane[0] = p.clip_plane[1] = p.clip_plane[2] = 0.0f;
        p.clip_plane[3] = 1e30f;
        for (int c = 0; c < 3; ++c) {
            p.field_scale[c] = vf.scale[c];
            p.field_bias[c] = vf.bias[c];
        }
        p.field_scale[3] = gain;
        p.field_bias[3] = vf.vmag_max;

        rime::rhi::TextureHandle target = swapchain->acquire_next_image();
        if (!target.is_valid()) {
            device->wait_idle();
            const Extent2D s = window->framebuffer_size();
            swapchain->recreate({s.width, s.height});
            device->destroy(depth);
            depth = make_depth(*device, swapchain->extent());
            continue;
        }
        auto cmd = device->begin_commands();
        rime::viewer::record_warp(*cmd, gpu, warp, target, depth, ext, p, kClear);
        rime::viewer::record_legend(*cmd, legend, target, ext);
        if (!swapchain->present(*cmd)) {
            device->wait_idle();
            const Extent2D s = window->framebuffer_size();
            swapchain->recreate({s.width, s.height});
            device->destroy(depth);
            depth = make_depth(*device, swapchain->extent());
        }
        ++frames;
    }

    device->wait_idle();
    device->destroy(depth);
    rime::viewer::destroy_legend(*device, legend);
    rime::viewer::destroy_warp(*device, warp);
    rime::viewer::destroy_mesh(*device, gpu);
    swapchain.reset();
    device.reset();
    shutdown();
    std::printf("icem_viewer: presented %d warped frame%s.\n", frames, frames == 1 ? "" : "s");
    return true;
}

// Build the raymarch push: inverse view-projection (for per-pixel rays), the world→uvw affine, the
// isovalue + value range, step count, and the iso/DVR mode.
rime::viewer::IsoPush make_iso_push(const OrbitCamera& cam,
                                    float aspect,
                                    const ScalarField& sf,
                                    float isovalue,
                                    bool dvr) {
    rime::viewer::IsoPush p{};
    p.inv_vp = rime::core::inverse(cam.view_proj(aspect));
    for (int c = 0; c < 3; ++c) {
        p.field_scale[c] = sf.scale[c];
        p.field_bias[c] = sf.bias[c];
    }
    p.field_scale[3] = isovalue;
    p.field_bias[3] = sf.vmin;
    p.meta[0] = sf.vmax;
    p.meta[1] = 192.0f; // raymarch steps
    p.meta[2] = dvr ? 1.0f : 0.0f;
    return p;
}

