int main(int argc, char** argv) {
    std::string stl_path;
    std::string field_path; // explicit --field; else a sibling <stem>.icef is tried
    std::string out_path = "icem_view.ppm";
    bool offscreen = false;
    bool no_field = false;
    bool no_cap = false;
    bool warp_mode = false; // --warp: animate the surface by a vec3 field (C3)
    std::string warp_name;  // optional vec3 field name to warp by (else the first vec3 field)
    bool flow_mode = false; // --flow: streamlines of a vec3 velocity field (D)
    std::string flow_name;  // optional vec3 velocity field name (else the first vec3 field)
    bool iso_mode = false;  // --iso: raymarched isosurface of a scalar field (C2)
    bool iso_given = false;
    float iso_value = 0.0f;
    bool dvr = false; // --dvr: direct volume rendering instead of an isosurface
    bool speed_mode =
        false; // --speed: colour/iso/DVR the scalar speed |v| of a vec3 velocity field (D2)
    std::string speed_name; // optional vec3 velocity field name (else the first vec3 field)
    std::string
        assembly_dir;     // --assembly <dir>: load every *.stl as a coloured, toggleable part (E1)
    bool explode = false; // --explode [factor]: start the assembly view exploded
    float explode_factor = 1.0f;
    bool prov_mode = false; // --provenance: show ICEM's "why" Ledger (.icejson) on the UI (E3)
    std::string prov_path;  // explicit .icejson path; else provenance.icejson / an STL sibling is tried
    bool turntable = false; // --turntable [N]: render a full 360° orbit to N numbered PPM frames (E4)
    int turntable_frames = 72; // default 72 frames = one every 5°
    std::uint32_t size = 512;
    int frames = 0;
    rime::core::Vec3 world_up{0.0f, 0.0f, 1.0f}; // ICEM parts are authored z-up
    Clip clip; // off unless --clip is given (offscreen); the window toggles it interactively

    for (int i = 1; i < argc; ++i) {
        const std::string_view a(argv[i]);
        if (a == "--offscreen" || a == "--headless") {
            offscreen = true;
            if (i + 1 < argc && argv[i + 1][0] != '-')
                out_path = argv[++i];
        } else if (a == "--windowed") {
            offscreen = false;
        } else if (a == "--field" && i + 1 < argc) {
            field_path = argv[++i];
        } else if (a == "--no-field") {
            no_field = true;
        } else if (a == "--no-cap") {
            no_cap = true;
        } else if (a == "--warp") {
            warp_mode = true;
            if (i + 1 < argc && argv[i + 1][0] != '-')
                warp_name = argv[++i];
        } else if (a == "--flow") {
            flow_mode = true;
            if (i + 1 < argc && argv[i + 1][0] != '-')
                flow_name = argv[++i];
        } else if (a == "--iso") {
            iso_mode = true;
            if (i + 1 < argc && argv[i + 1][0] != '-') {
                iso_value = static_cast<float>(std::atof(argv[++i]));
                iso_given = true;
            }
        } else if (a == "--dvr") {
            iso_mode = true;
            dvr = true;
        } else if (a == "--speed") {
            speed_mode = true;
            if (i + 1 < argc && argv[i + 1][0] != '-')
                speed_name = argv[++i];
        } else if (a == "--assembly") {
            assembly_dir = (i + 1 < argc && argv[i + 1][0] != '-') ? argv[++i] : ".";
        } else if (a == "--explode") {
            explode = true;
            if (i + 1 < argc && argv[i + 1][0] != '-')
                explode_factor = static_cast<float>(std::atof(argv[++i]));
        } else if (a == "--provenance") {
            prov_mode = true;
            if (i + 1 < argc && argv[i + 1][0] != '-')
                prov_path = argv[++i];
        } else if (a == "--turntable") {
            turntable = true;
            if (i + 1 < argc && argv[i + 1][0] != '-')
                turntable_frames = std::max(1, std::atoi(argv[++i]));
        } else if (a == "--clip" && i + 1 < argc) {
            const std::string_view ax(argv[++i]);
            clip.on = true;
            clip.axis = (ax == "y") ? 1 : (ax == "z") ? 2 : 0;
        } else if (a == "--size" && i + 1 < argc) {
            size = static_cast<std::uint32_t>(std::max(8, std::atoi(argv[++i])));
        } else if (a == "--frames" && i + 1 < argc) {
            frames = std::atoi(argv[++i]);
        } else if (a == "--up-y") {
            world_up = {0.0f, 1.0f, 0.0f};
        } else if (!a.empty() && a[0] != '-') {
            stl_path = a;
        }
    }

    // --provenance: surface ICEM's "why" Ledger (.icejson) on the from-scratch UI (E3). A panel-only
    // view — no 3-D mesh — so it dispatches before any STL/assembly loading. The file is an explicit
    // --provenance <path>, else `provenance.icejson` (what `icem provenance <dir>` writes), else an
    // STL sibling <stem>.icejson.
    if (prov_mode) {
        std::string ppath = prov_path;
        if (ppath.empty())
            ppath = stl_path.empty() ? "provenance.icejson" : replace_extension(stl_path, ".icejson");
        auto prov = rime::viewer::load_provenance(ppath);
        if (!prov) {
            std::fprintf(stderr,
                         "icem_viewer: could not load ICEM provenance '%s' "
                         "(generate one with `icem provenance <dir>`)\n",
                         ppath.c_str());
            return 1;
        }
        if (offscreen)
            return run_provenance_offscreen(*prov, out_path, size);
        if (run_provenance_windowed(*prov, frames))
            return 0;
        return run_provenance_offscreen(*prov, out_path, size);
    }

    // --assembly <dir> (or a directory passed as the positional path): load every *.stl in it as a
    // coloured, individually toggleable part and show them together, with an exploded view (E1).
    {
        std::error_code ec;
        if (assembly_dir.empty() && !stl_path.empty() &&
            std::filesystem::is_directory(stl_path, ec))
            assembly_dir = stl_path;
    }
    if (!assembly_dir.empty()) {
        auto assembly = rime::viewer::load_assembly(assembly_dir);
        if (!assembly) {
            std::fprintf(
                stderr, "icem_viewer: no loadable STL parts in '%s'\n", assembly_dir.c_str());
            return 1;
        }
        const float factor = explode ? explode_factor : 0.0f;
        if (offscreen)
            return run_assembly_offscreen(*assembly, out_path, size, world_up, factor);
        if (run_assembly_windowed(*assembly, world_up, frames, factor))
            return 0;
        return run_assembly_offscreen(*assembly, out_path, size, world_up, factor);
    }

    CpuMesh mesh;
    if (!stl_path.empty()) {
        auto loaded = rime::viewer::load_stl_binary_file(stl_path);
        if (!loaded) {
            std::fprintf(stderr, "icem_viewer: could not load binary STL '%s'\n", stl_path.c_str());
            return 1;
        }
        mesh = std::move(*loaded);
    } else {
        std::printf(
            "icem_viewer: no STL given — showing a unit cube. Pass a binary STL to view a part.\n");
        mesh = rime::viewer::make_unit_cube();
    }

    // --warp: animate the surface by a computed vec3 field (displacement / modal mode shape). It
    // reads the same .icef (explicit --field, else the STL's sibling), picking a vector field by
    // name.
    if (warp_mode) {
        const std::string vpath = field_path.empty() ? sibling_icef(stl_path) : field_path;
        auto vfield = rime::viewer::load_icef_vector(vpath, warp_name);
        if (!vfield) {
            std::fprintf(
                stderr,
                "icem_viewer: --warp needs a vec3 field in '%s' (e.g. displacement or mode1)\n",
                vpath.c_str());
            return 1;
        }
        std::printf("icem_viewer: warp field '%s' [%s] |max| %.4g on a %ux%ux%u grid (%s)\n",
                    vfield->name.c_str(),
                    vfield->unit.c_str(),
                    static_cast<double>(vfield->vmag_max),
                    vfield->nx,
                    vfield->ny,
                    vfield->nz,
                    vpath.c_str());
        if (offscreen)
            return run_warp_offscreen(mesh, out_path, size, world_up, *vfield);
        if (run_warp_windowed(mesh, world_up, frames, *vfield))
            return 0;
        return run_warp_offscreen(mesh, out_path, size, world_up, *vfield);
    }

    // --flow: streamlines of a computed 3-D velocity field (ICEM's potential-flow .icef, brick24).
    if (flow_mode) {
        const std::string vpath = field_path.empty() ? sibling_icef(stl_path) : field_path;
        auto vfield = rime::viewer::load_icef_vector(vpath, flow_name);
        if (!vfield) {
            std::fprintf(
                stderr, "icem_viewer: --flow needs a vec3 velocity field in '%s'\n", vpath.c_str());
            return 1;
        }
        std::printf("icem_viewer: flow field '%s' [%s] |u|max %.4g on a %ux%ux%u grid (%s)\n",
                    vfield->name.c_str(),
                    vfield->unit.c_str(),
                    static_cast<double>(vfield->vmag_max),
                    vfield->nx,
                    vfield->ny,
                    vfield->nz,
                    vpath.c_str());
        if (offscreen)
            return run_flow_offscreen(mesh, out_path, size, world_up, *vfield);
        if (run_flow_windowed(mesh, world_up, frames, *vfield))
            return 0;
        return run_flow_offscreen(mesh, out_path, size, world_up, *vfield);
    }

    // A scalar field for the colormap / legend / isosurface / DVR: either a scalar straight from
    // the .icef, or — with --speed — the magnitude |v| derived from a vec3 velocity field (D2·V),
    // so the viscous boundary layer can be shown as a coloured surface, isotach or DVR volume, not
    // only as lines.
    const auto load_scalar = [&](const std::string& path) -> std::optional<ScalarField> {
        if (speed_mode) {
            auto vf = rime::viewer::load_icef_vector(path, speed_name);
            if (!vf)
                return std::nullopt;
            return rime::viewer::speed_field(*vf);
        }
        return rime::viewer::load_icef_scalar(path);
    };

    // --iso / --dvr: GPU raymarched isosurface or DVR of a scalar field (C2; or the speed scalar,
    // D2·V).
    if (iso_mode) {
        const std::string spath = field_path.empty() ? sibling_icef(stl_path) : field_path;
        auto sf = load_scalar(spath);
        if (!sf) {
            std::fprintf(stderr,
                         "icem_viewer: --iso needs a %s in '%s'\n",
                         speed_mode ? "vec3 velocity field (--speed)" : "scalar field",
                         spath.c_str());
            return 1;
        }
        const float iso = iso_given ? iso_value : 0.5f * (sf->vmin + sf->vmax);
        std::printf("icem_viewer: %s field '%s' [%s] range %.4g .. %.4g, iso %.4g (%s)\n",
                    dvr ? "DVR of" : "isosurface of",
                    sf->name.c_str(),
                    sf->unit.c_str(),
                    static_cast<double>(sf->vmin),
                    static_cast<double>(sf->vmax),
                    static_cast<double>(iso),
                    spath.c_str());
        if (offscreen)
            return run_iso_offscreen(mesh, out_path, size, world_up, *sf, iso, dvr);
        if (run_iso_windowed(mesh, world_up, frames, *sf, iso, dvr))
            return 0;
        return run_iso_offscreen(mesh, out_path, size, world_up, *sf, iso, dvr);
    }

    // Load a simulation field: an explicit --field path, else the STL's sibling <stem>.icef if
    // present.
    std::optional<ScalarField> field;
    const bool explicit_field = !field_path.empty();
    if (field_path.empty())
        field_path = sibling_icef(stl_path);
    if (!field_path.empty()) {
        field = load_scalar(field_path);
        if (field) {
            std::printf(
                "icem_viewer: loaded field '%s' [%s] range %.4g .. %.4g on a %ux%ux%u grid (%s)\n",
                field->name.c_str(),
                field->unit.c_str(),
                static_cast<double>(field->vmin),
                static_cast<double>(field->vmax),
                field->nx,
                field->ny,
                field->nz,
                field_path.c_str());
        } else if (explicit_field) {
            std::fprintf(stderr, "icem_viewer: could not load field '%s'\n", field_path.c_str());
        }
    }

    const ScalarField* fptr = field ? &*field : nullptr;
    const bool field_on = field.has_value() && !no_field;
    const bool cap_on = !no_cap;

    // --turntable: a headless 360° orbit to a numbered PPM sequence (honours --clip / --field), no
    // window. Dispatched before the windowed/offscreen single-shot since it is its own export mode.
    if (turntable)
        return run_turntable_offscreen(
            mesh, out_path, size, world_up, clip, fptr, field_on, cap_on, turntable_frames);

    if (offscreen)
        return run_offscreen(mesh, out_path, size, world_up, clip, fptr, field_on, cap_on);
    if (run_windowed(mesh, world_up, frames, fptr, field_on, cap_on))
        return 0;
    std::printf("icem_viewer: no window/display — falling back to off-screen (%s).\n",
                out_path.c_str());
    return run_offscreen(mesh, out_path, size, world_up, clip, fptr, field_on, cap_on);
}
